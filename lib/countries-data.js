export const validatePhoneNumber = (countryCode, phoneNumber) => {
  const pattern = phoneValidationPatterns[countryCode]
  if (!pattern) {
    // Generic validation for countries without specific patterns
    return {
      isValid: /^\d{7,15}$/.test(phoneNumber),
      error: phoneNumber.length < 7 ? "Phone number too short" : 
             phoneNumber.length > 15 ? "Phone number too long" : 
             "Invalid phone number format"
    }
  }
  
  const isValid = pattern.pattern.test(phoneNumber)
  let error = ""
  
  if (!isValid) {
    if (phoneNumber.length < pattern.minLength) {
      error = `Phone number too short (minimum ${pattern.minLength} digits)`
    } else if (phoneNumber.length > pattern.maxLength) {
      error = `Phone number too long (maximum ${pattern.maxLength} digits)`
    } else {
      error = `Invalid format. Example: ${countryCode}${pattern.example}`
    }
  }
  
  return { isValid, error }
}
